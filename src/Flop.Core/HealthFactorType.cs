namespace Flop.Core;

/// <summary>
/// An enum representing the different possible health factors in the game.
/// </summary>
public enum HealthFactorType
{
    /// <summary>
    /// The health factor for the actor's hit points.
    /// </summary>
    HitPoints,

    /// <summary>
    /// The health factor for the actor's blood loss.
    /// </summary>
    BloodLoss,

    /// <summary>
    /// The health factor for the actor's poison.
    /// </summary>
    Poison,
}
